from __future__ import annotations

import json
import re
from pathlib import Path
from typing import Any, Dict, Optional, Sequence, Tuple

import cv2
import numpy as np
import torch
from torch.utils.data import Dataset

from .vkitti2_raw import (
    _crop_hwew,
    _imagenet_normalize_rgb_tensor,
    _resize_rgb_short_edge,
    _rgb_preview_tensor,
)


TEMP_PATH_MARKERS = ("codex_smoke", "smoke", "debug", "tmp")


def _read_json(path: Path) -> Dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def _read_manifest(path: Path) -> Sequence[Dict[str, Any]]:
    records = []
    with path.open("r", encoding="utf-8") as handle:
        for line_no, line in enumerate(handle, start=1):
            line = line.strip()
            if not line:
                continue
            record = json.loads(line)
            record.setdefault("_line_no", line_no)
            records.append(record)
    return records


def _read_filelist(path: Path) -> Sequence[Tuple[str, str]]:
    rows = []
    with path.open("r", encoding="utf-8") as handle:
        for line_no, line in enumerate(handle, start=1):
            parts = line.strip().split()
            if not parts:
                continue
            if len(parts) != 2:
                raise ValueError(f"Expected two columns in {path}:{line_no}, got {len(parts)}")
            rows.append((parts[0], parts[1]))
    return rows


def _path_has_temp_marker(path: Path) -> bool:
    lowered = str(path).lower()
    return any(marker in lowered for marker in TEMP_PATH_MARKERS)


def _infer_variant_count(cache_root: Path, config: Dict[str, Any]) -> int:
    for key in ("num_variants", "variants", "k"):
        value = config.get(key)
        if value is None:
            continue
        try:
            count = int(value)
        except (TypeError, ValueError):
            continue
        if count >= 1:
            return count

    match = re.search(r"(?:^|[_-])k(\d+)(?:\D|$)", cache_root.name)
    if match:
        return max(int(match.group(1)), 1)
    return 1


def _build_cache_tag(cache_root: Path, config: Dict[str, Any], num_variants: int) -> str:
    tag = config.get("cache_tag")
    if tag:
        return str(tag)

    storage = dict(config.get("storage", {}))
    raw_dtype = str(storage.get("raw_dtype", "unknown"))
    dtype_tag = {
        "float16": "fp16",
        "float32": "fp32",
    }.get(raw_dtype, raw_dtype)
    randomized = bool(config.get("randomize_unprocessing", False))
    rand_suffix = "rand" if randomized else "fixed"
    return f"vkcachek{num_variants}{rand_suffix}_{dtype_tag}"


class CachedVKITTI2Raw(Dataset):
    """Read deterministic VKITTI2 pseudo-RAW samples generated by cache_vkitti2_pseudoraw.py."""

    def __init__(
        self,
        cache_root: str | Path,
        *,
        filelist_path: Optional[str | Path] = None,
        size: Optional[Tuple[int, int]] = None,
        expected_num_samples: Optional[int] = None,
        expected_preset_hash: Optional[str] = None,
        allow_partial: Optional[bool] = None,
    ) -> None:
        self.cache_root = Path(cache_root).expanduser().resolve()
        self.config_path = self.cache_root / "config.json"
        self.manifest_path = self.cache_root / "manifest.jsonl"
        if not self.config_path.is_file():
            raise FileNotFoundError(f"Missing cache config: {self.config_path}")
        if not self.manifest_path.is_file():
            raise FileNotFoundError(f"Missing cache manifest: {self.manifest_path}")

        self.config = _read_json(self.config_path)
        self.records = list(_read_manifest(self.manifest_path))
        self.allow_partial = _path_has_temp_marker(self.cache_root) if allow_partial is None else bool(allow_partial)

        self.size = tuple(int(v) for v in self.config.get("size", ()))
        if size is not None and self.size != tuple(int(v) for v in size):
            raise ValueError(f"Cache size mismatch: cache={self.size}, expected={tuple(size)}")

        config_num_samples = int(self.config.get("num_samples", len(self.records)))
        if config_num_samples != len(self.records):
            if not (self.allow_partial and len(self.records) <= config_num_samples):
                raise ValueError(
                    f"Cache num_samples mismatch: config={config_num_samples}, manifest={len(self.records)}"
                )
        if expected_num_samples is not None and config_num_samples != int(expected_num_samples):
            if not (self.allow_partial and config_num_samples <= int(expected_num_samples)):
                raise ValueError(
                    f"Cache sample count mismatch: cache={config_num_samples}, expected={expected_num_samples}"
                )

        unprocessing = self.config.get("unprocessing", {})
        self.preset_hash = str(unprocessing.get("preset_hash", ""))
        if expected_preset_hash is not None and self.preset_hash != str(expected_preset_hash):
            raise ValueError(
                f"Cache preset_hash mismatch: cache={self.preset_hash}, expected={expected_preset_hash}"
            )

        self.mode = str(self.config.get("mode", "train"))
        self.min_depth = float(self.config.get("min_depth", 1.0))
        self.max_depth = float(self.config.get("max_depth", 80.0))
        self.hflip_prob = float(self.config.get("hflip_prob", 0.0))
        self.seed = int(self.config.get("seed", 0))
        self.storage = dict(self.config.get("storage", {}))
        self.num_variants = _infer_variant_count(self.cache_root, self.config)
        self.cache_tag = _build_cache_tag(self.cache_root, self.config, self.num_variants)
        self.storage_format = str(self.storage.get("format", ""))
        if self.storage_format and self.storage_format != "torch_pt_per_sample":
            raise ValueError(f"Unsupported VKITTI cache storage format: {self.storage_format}")

        if filelist_path is not None:
            self.filelist_path = Path(filelist_path).expanduser().resolve()
            self._validate_filelist(self.filelist_path)
        else:
            self.filelist_path = Path(str(self.config.get("filelist_path", ""))).expanduser()

    def _validate_filelist(self, filelist_path: Path) -> None:
        filelist_rows = _read_filelist(filelist_path)
        if len(self.records) > len(filelist_rows):
            raise ValueError(
                f"Cache has more manifest rows than filelist entries: {len(self.records)} > {len(filelist_rows)}"
            )
        if not self.allow_partial and len(self.records) != len(filelist_rows):
            raise ValueError(
                f"Cache/filelist row count mismatch: cache={len(self.records)}, filelist={len(filelist_rows)}"
            )
        for idx, (record, (image_path, depth_path)) in enumerate(zip(self.records, filelist_rows)):
            if str(record.get("image_path")) != image_path or str(record.get("depth_path")) != depth_path:
                raise ValueError(
                    "Cache/filelist path mismatch at idx="
                    f"{idx}: cache=({record.get('image_path')}, {record.get('depth_path')}), "
                    f"filelist=({image_path}, {depth_path})"
                )

    def __len__(self) -> int:
        return len(self.records)

    def describe_unprocessing(self) -> Dict[str, Any]:
        unprocessing = dict(self.config.get("unprocessing", {}))
        return {
            "unprocessing_preset": unprocessing.get("preset_name"),
            "unprocessing_kind": unprocessing.get("preset_kind"),
            "preset_version": unprocessing.get("preset_version"),
            "preset_hash": unprocessing.get("preset_hash"),
            "isp_profile_group": unprocessing.get("isp_profile_group"),
            "sub_presets": unprocessing.get("sub_presets", []),
            "default_sub_preset": unprocessing.get("default_sub_preset"),
            "mix_weights": unprocessing.get("mix_weights", {}),
            "active_transform": unprocessing.get("active_transform", {}),
            "sub_preset_transforms": unprocessing.get("sub_preset_transforms", {}),
        }

    def __getitem__(self, idx: int) -> Dict[str, Any]:
        record = self.records[idx]
        cache_path = Path(str(record["cache_path"]))
        if not cache_path.is_absolute():
            cache_path = self.cache_root / cache_path
        payload = torch.load(cache_path, map_location="cpu")
        raw = payload["raw"].float().contiguous()
        depth = payload["depth"].float().contiguous()
        valid_mask = payload["valid_mask"]
        if valid_mask.dtype == torch.bool:
            valid_mask_bool = valid_mask.contiguous()
        else:
            valid_mask_bool = (valid_mask > 0).contiguous()

        expected_raw_shape = (4, *self.size)
        expected_depth_shape = self.size
        if tuple(raw.shape) != expected_raw_shape:
            raise ValueError(
                f"Cached VKITTI raw shape mismatch at idx={idx}: "
                f"got={tuple(raw.shape)} expected={expected_raw_shape} path={cache_path}"
            )
        if tuple(depth.shape) != expected_depth_shape:
            raise ValueError(
                f"Cached VKITTI depth shape mismatch at idx={idx}: "
                f"got={tuple(depth.shape)} expected={expected_depth_shape} path={cache_path}"
            )
        if tuple(valid_mask_bool.shape) != expected_depth_shape:
            raise ValueError(
                f"Cached VKITTI valid_mask shape mismatch at idx={idx}: "
                f"got={tuple(valid_mask_bool.shape)} expected={expected_depth_shape} path={cache_path}"
            )

        sample = {
            "raw": raw,
            "depth": depth,
            "valid_mask": valid_mask_bool,
            "sample_name": str(payload.get("sample_name", record.get("sample_name", idx))),
            "image_path": str(payload.get("image_path", record.get("image_path", ""))),
            "depth_path": str(payload.get("depth_path", record.get("depth_path", ""))),
            "target_space": "metric_depth",
        }
        geometry_params = payload.get("geometry_params", record.get("geometry_params"))
        if geometry_params is not None:
            sample["geometry_params"] = geometry_params
        if "isp_params" in payload:
            sample["isp_params"] = payload["isp_params"]
        return sample

    def build_sample(self, idx: int, *, include_geometry: bool = False, **_: Any) -> Dict[str, Any]:
        sample = self[idx]
        if include_geometry and "geometry_params" not in sample:
            sample["geometry_params_missing"] = True
        return sample

    def build_rgb_baseline_input(
        self,
        idx: int,
        geometry: Dict[str, Any],
        *,
        target_hw: Tuple[int, int],
    ) -> Tuple[torch.Tensor, torch.Tensor]:
        if not geometry or geometry.get("crop_box") is None:
            raise ValueError(
                "Cached VKITTI RGB baseline requires geometry_params in the cache payload/manifest. "
                f"Rebuild the cache with geometry metadata: cache_root={self.cache_root} idx={idx}"
            )
        record = self.records[idx]
        img_path = Path(str(record.get("image_path", ""))).expanduser()
        if not img_path.is_absolute():
            img_path = img_path.resolve()
        image = cv2.imread(str(img_path), cv2.IMREAD_COLOR)
        if image is None:
            raise ValueError(f"Failed to read cached VKITTI source RGB for baseline: {img_path}")
        image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB).astype(np.float32) / 255.0
        fullres_size = (int(self.size[0]) * 2, int(self.size[1]) * 2)
        image = _resize_rgb_short_edge(image, short_edge=fullres_size[0])
        image = _crop_hwew(image, tuple(geometry["crop_box"]))
        if bool(geometry.get("hflip_applied", False)):
            image = np.ascontiguousarray(image[:, ::-1])
        return _imagenet_normalize_rgb_tensor(image, target_hw), _rgb_preview_tensor(image, target_hw)
